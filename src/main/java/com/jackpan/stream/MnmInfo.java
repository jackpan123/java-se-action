package com.jackpan.stream;

/**
 * MnmInfo操作类
 *
 * @author JackPan
 * @date 2022/05/22 09:16
 **/
public class MnmInfo {

    /**
     * 州名
     */
    private String ca;

    /**
     * 颜色
     */
    private String color;

    /**
     * 总数
     */
    private int count;

    public String getCa() {
        return ca;
    }

    public void setCa(String ca) {
        this.ca = ca;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
